create_base_user_table = """
CREATE TABLE IF NOT EXISTS BaseUser (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
USER_NAME TEXT UNIQUE NOT NULL,
EMAIL TEXT UNIQUE NOT NULL,
PHONE_NUMBER VARCHAR(20) UNIQUE NOT NULL,
ADDRESS TEXT
)
"""

create_client_table = """
CREATE TABLE IF NOT EXISTS Client (
BASE_ID INTEGER PRIMARY KEY AUTOINCREMENT,
FOREIGN KEY (BASE_ID) REFERENCES BaseUser(ID)
)
"""

create_mechanic_table = """
CREATE TABLE IF NOT EXISTS Mechanic (
BASE_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TITLE VARCHAR(20)
FOREIGN KEY (BASE_ID) REFERENCES BaseUser(ID)
)
"""

create_vehicle_table = """
CREATE TABLE IF NOT EXISTS Vehicle (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
CATEGORY TEXT,
MAKE TEXT NOT NULL,
MODEL VARCHAR(50) NOT NULL,
REGISTER_NUMBER TEXT UNIQUE NOT NULL,
GEAR_BOX TEXT NOT NULL,
OWNER INTEGER,
FOREIGN KEY (OWNER) REFERENCES Client(BASE_ID)
)
"""

create_repair_hour_table = """
CREATE TABLE IF NOT EXISTS RepairHour (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
DATE TEXT NOT NULL,
START_HOUR TEXT NOT NULL,
VEHICLE INTEGER NOT NULL,
BILL REAL NOT NULL,
MECHANIC_SERVICE INTEGER NOT NULL,
FOREIGN KEY (VEHICLE) REFERENCES Vehicle(ID)
FOREIGN KEY (MECHANIC_SERVICE) REFERENCES MechanicalService(ID)
)
"""

create_mechanic_service_table = """
CREATE TABLE IF NOT EXISTS MechanicalService (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
MECHANIC_ID INTEGER NOT NULL,
SERVICE_ID INTEGER NOT NULL,
FOREIGN KEY MECHANIC_ID REFERENCES Mechanic(ID)
FOREIGN KEY SERVICE_ID REFERENCES Service(ID)
)
"""

create_service_table = """
CREATE TABLE IF NOT EXISTS Service (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
NAME VARCHAR(50) UNIQUE NOT NULL
)
"""